export function ModelAvatar({ modelName, className = "" }: ModelAvatarProps) {
  if (!modelName) {
    return <span className={`font-black tracking-tighter ${className}`}>AI</span>;
  }

  const name = modelName.toLowerCase();

  // Gemini models
  if (name.includes("gemini") || name.includes("google")) {
    return <GeminiLogo className={`w-5 h-5 text-blue-500 ${className}`} />;
  }

  // Claude / Anthropic
  if (name.includes("claude") || name.includes("anthropic")) {
    return <AnthropicLogo className={`w-5 h-5 text-amber-500 ${className}`} />;
  }

  // DeepSeek
  if (name.includes("deepseek")) {
    return <DeepSeekLogo className={`w-5 h-5 text-blue-600 ${className}`} />;
  }

  // OpenAI / GPT
  if (
    name.includes("gpt") ||
    name.includes("openai") ||
    name.includes("o1") ||
    name.includes("o3")
  ) {
    return <OpenAILogo className={`w-5 h-5 text-emerald-600 ${className}`} />;
  }

  // Groq / Llama (fast inference)
  if (name.includes("groq") || name.includes("llama") || name.includes("mixtral")) {
    return <Zap className={`w-5 h-5 text-orange-500 ${className}`} />;
  }

  // Default / Generic AI
  return <Brain className={`w-5 h-5 text-purple-500 ${className}`} />;
}
